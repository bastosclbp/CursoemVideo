a = int(input('Informe o primeiro número inteiro: '))
b = int(input('Informe o segundo número inteiro: '))
c = float(input('Informe um número real: '))
print('O produto do dobro do primeiro com metade do segundo é: {}'.format(
    (a * 2) + (b / 2)))
print('A soma do triplo do primeiro com o terceiro é: {}'.format((a * 3) + c))
print('O terceiro elevado ao cubo é: {}'.format(c ** 3))
